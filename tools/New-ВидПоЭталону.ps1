# New-ВидПоЭталону.ps1 — этап 1 конвейера: генерация Catalogs/<Вид> из шаблонов по JSON-карте
# + регистрация в Configuration.mdo. Dry-run по умолчанию, -Apply — писать.
# UUID детерминированные (SHA1 от uuid вида/свойства) — идемпотентность и стабильная
# реструктуризация. Требует pwsh 7.

[CmdletBinding()]
param(
    [Parameter(Mandatory)] [string]$Карта,
    [string[]]$Виды,
    [string]$Src = 'E:\1CRoot\EDTPasyanizator\До_КОРП_30_0407\src',
    [switch]$Apply
)

$ErrorActionPreference = 'Stop'
. "$PSScriptRoot\Пасьянс-Общее.ps1"

$Шаблоны = "$PSScriptRoot\Шаблоны"
$CatalogTpl = (Read-Текст "$Шаблоны\Catalog.mdo.tpl").Текст
$AttrTpl = (Read-Текст "$Шаблоны\Attribute.xml.tpl").Текст
$ObjTpl = (Read-Текст "$Шаблоны\ObjectModule.bsl.tpl").Текст
$MgrTpl = (Read-Текст "$Шаблоны\ManagerModule.bsl.tpl").Текст
$FormTpl = (Read-Текст "$Шаблоны\ФормаЭлемента.form.tpl").Текст
$FieldTpl = (Read-Текст "$Шаблоны\ПолеСписочное.form.tpl").Текст
$FormModTpl = (Read-Текст "$Шаблоны\ФормаМодуль.bsl.tpl").Текст
$ТипыРекв = Get-Content "$Шаблоны\ТипыРеквизитов.json" -Raw -Encoding utf8 | ConvertFrom-Json

$к = Read-ПасьянсКарта $Карта
$отчёт = [System.Collections.Generic.List[object]]::new()

function Рендер-Типы($типы) {
    $строкиТипов = @()
    $квалификаторы = @()
    foreach ($т in $типы) {
        $вид = $ТипыРекв.виды.($т.вид)
        if (-not $вид) { throw "вид типа '$($т.вид)' не покрыт ТипыРеквизитов.json" }
        $строкаТипа = $вид.types
        if ($т.ссылочныйТип) { $строкаТипа = $строкаТипа.Replace('{{ССЫЛОЧНЫЙТИП}}', $т.ссылочныйТип) }
        $строкиТипов += $строкаТипа
        if ($вид.квалификаторы) {
            $кв = $вид.квалификаторы
            if ($null -ne $т.длина) { $кв = $кв.Replace('{{ДЛИНА}}', [string]$т.длина) }
            if ($null -ne $т.разрядность) { $кв = $кв.Replace('{{РАЗРЯДНОСТЬ}}', [string]$т.разрядность) }
            $кв = $кв.Replace('{{ТОЧНОСТЬ}}', [string]([int]($т.точность ?? 0)))
            $квалификаторы += $кв
        }
    }
    ($строкиТипов + $квалификаторы) -join "`n"
}

# дефолтные поля видов (шаблоны сняты с ДокументыПредприятия; ТЧ — такт 4+)
$ДефолтныеПоля = @{
    'Организация' = @{ синоним = 'Организация'
        типы = @([pscustomobject]@{ вид = 'Ссылка'; ссылочныйТип = 'CatalogRef.Организации' })
        индексировать = $true }
    'Сумма' = @{ синоним = 'Сумма'
        типы = @([pscustomobject]@{ вид = 'Число'; разрядность = 15; точность = 2 }) }
}

foreach ($в in $к.виды) {
    $имя = $в.справочник.имя
    if ($Виды -and $имя -notin $Виды) { continue }
    $видUuid = $в.вид.uuid

    # Зомби-каталог (пре-ревью такта 4): каталог опознаётся ПО ИМЕНИ, а все UUID сеются от uuid
    # ВИДА. Если имя справочника сменилось (правка наименования вида / суффикс коллизии), повторный
    # прогон создаст ВТОРОЙ Catalog с теми же детерминированными UUID — дубль uuid в конфе, который
    # не ловит ни один гейт этапов 0-3. Ищем свой вид под ДРУГИМ именем и падаем внятно.
    $каталогиДир = Join-Path $Src 'Catalogs'
    if (Test-Path $каталогиДир) {
        $двойники = @(Get-ChildItem $каталогиДир -Directory | Where-Object { $_.Name -ne $имя } | Where-Object {
                $m = Join-Path $_.FullName "$($_.Name).mdo"
                (Test-Path $m) -and ([System.IO.File]::ReadAllText($m).Contains("Пасьянизатор из вида $видUuid"))
            } | ForEach-Object Name)
        if ($двойники) {
            throw "вид ${имя}: тот же вид (uuid $видUuid) уже сгенерён под именем '$($двойники -join ', ')' — переименуй каталог+регистрацию в Configuration.mdo или удали старый (иначе дубль детерминированных UUID в конфе)"
        }
    }

    # П2.8 (контракт такта, ревью П2-линзы-2): EAV-хвост НЕ молчит и НЕ валит — механизм
    # свойств (ТЧ+predefined+резолв) реализует такт 5, до тех пор хвост объявляется явно
    $хвостEAV = @($в.остаютсяВEAV | Where-Object { $_ }).Count
    $хвостСведений = @($в.допСведения | Where-Object { $_ }).Count
    if ($хвостEAV -gt 0 -or $хвостСведений -gt 0) {
        Write-Warning "${имя}: EAV-хвост — реквизитов $хвостEAV, сведений $хвостСведений; механизм свойств — такт 5 (поля НЕ генерятся, данные остаются в EAV)"
    }

    # реквизиты: сначала дефолтные поля (тащить=true), затем ПВХ-реквизиты карты
    $блокиРеквизитов = @()
    foreach ($п in $в.дефолтныеПоля) {
        if (-not $п.тащить) { continue }
        # заказанное, но не сгенерённое поле = молча потерянная колонка данных в Фазе 4 — стоп
        if (-not $п.имя) { throw "вид ${имя}: дефолтное поле с тащить=true без имени — карта битая" }
        $шаблон = $ДефолтныеПоля[$п.имя]
        if (-not $шаблон) { throw "вид ${имя}: дефолтное поле '$($п.имя)' не покрыто шаблонами генератора (есть только: $($ДефолтныеПоля.Keys -join ', '))" }
        $блок = $AttrTpl
        $блок = $блок.Replace('{{UUID}}', (New-ДетерминированныйUuid "Пасьянс|Attr|$видUuid|Дефолт|$($п.имя)"))
        $блок = $блок.Replace('{{ИМЯ}}', $п.имя)
        $блок = $блок.Replace('{{СИНОНИМ}}', $шаблон.синоним)
        $блок = $блок.Replace('{{ТИПЫ}}', (Рендер-Типы $шаблон.типы))
        $опции = @()
        if ($шаблон.индексировать) { $опции += $ТипыРекв.опции.индексировать }
        $блок = $блок.Replace('{{ОПЦИИ}}', ($опции -join "`n")).Replace("`n`n  </attributes>", "`n  </attributes>")
        $блокиРеквизитов += $блок.TrimEnd()
    }
    foreach ($р in $в.реквизиты) {
        $блок = $AttrTpl
        $блок = $блок.Replace('{{UUID}}', (New-ДетерминированныйUuid "Пасьянс|Attr|$видUuid|$($р.свойство)"))
        $блок = $блок.Replace('{{ИМЯ}}', $р.имяРеквизита)
        $блок = $блок.Replace('{{СИНОНИМ}}', $р.заголовок)
        $блок = $блок.Replace('{{ТИПЫ}}', (Рендер-Типы $р.типы))
        $опции = @()
        if ($р.обязательный) { $опции += $ТипыРекв.опции.обязательный }
        if ($р.индексировать) { $опции += $ТипыРекв.опции.индексировать }
        $блок = $блок.Replace('{{ОПЦИИ}}', ($опции -join "`n")).Replace("`n`n  </attributes>", "`n  </attributes>")
        $блокиРеквизитов += $блок.TrimEnd()
    }
    # Такт 5 / П3: приземлённые СВЕДЕНИЯ (решение=реквизитОбъекта) — те же attribute-блоки, но сид
    # UUID с СОЛЬЮ |Свед| (отдельное пространство: сведение и реквизит с одним именем реквизита в
    # одном виде не коллизят по UUID; имена дедуплены экстрактором против общего ЗанятыеИмена).
    $сведенияКолонки = @($в.допСведения | Where-Object { $_.решение -eq 'реквизитОбъекта' })
    foreach ($с in $сведенияКолонки) {
        $блок = $AttrTpl
        $блок = $блок.Replace('{{UUID}}', (New-ДетерминированныйUuid "Пасьянс|Attr|$видUuid|Свед|$($с.свойство)"))
        $блок = $блок.Replace('{{ИМЯ}}', $с.имяРеквизита)
        $блок = $блок.Replace('{{СИНОНИМ}}', $с.заголовок)
        $блок = $блок.Replace('{{ТИПЫ}}', (Рендер-Типы $с.типы))
        $опции = @()
        if ($с.обязательный) { $опции += $ТипыРекв.опции.обязательный }
        if ($с.индексировать) { $опции += $ТипыРекв.опции.индексировать }
        $блок = $блок.Replace('{{ОПЦИИ}}', ($опции -join "`n")).Replace("`n`n  </attributes>", "`n  </attributes>")
        $блокиРеквизитов += $блок.TrimEnd()
    }

    # шапка справочника
    $mdo = $CatalogTpl
    foreach ($роль in 'OBJECT_T','OBJECT_V','REF_T','REF_V','SELECTION_T','SELECTION_V','LIST_T','LIST_V','MANAGER_T','MANAGER_V') {
        $mdo = $mdo.Replace("{{UUID_$роль}}", (New-ДетерминированныйUuid "Пасьянс|$роль|$видUuid"))
    }
    $mdo = $mdo.Replace('{{UUID_ВИДДОКУМЕНТА}}', (New-ДетерминированныйUuid "Пасьянс|Attr|$видUuid|ВидДокумента"))
    $mdo = $mdo.Replace('{{UUID}}', (New-ДетерминированныйUuid "Пасьянс|Catalog|$видUuid"))
    $mdo = $mdo.Replace('{{ИМЯ}}', $имя)
    $mdo = $mdo.Replace('{{СИНОНИМ}}', "$($в.справочник.синоним) (Пасьянизатор)")
    $mdo = $mdo.Replace('{{ВИД_UUID}}', $видUuid)
    $mdo = $mdo.Replace('{{РЕКВИЗИТЫ}}', ($блокиРеквизитов -join "`n"))
    $mdo = $mdo.Replace('{{ТАБЛИЧНЫЕ_ЧАСТИ}}', '')   # такт 2: ТЧ не генерим (native-ТЧ — отдельный такт)

    # Такт 5 / П2: форма элемента с ПРОГРАММНЫМ отбором владельца для списочных реквизитов.
    # Фиксированный Отбор.Владелец в .mdo НЕВОЗМОЖЕН (владелец = данные, не predefined) — ставим в
    # ПриСозданииНаСервере формы (UUID свойства из карты). Форму генерим ТОЛЬКО при списочных.
    # Такт 5 П4 (пре-ревью D3): приземлённое СПИСОЧНОЕ сведение (решение=реквизитОбъекта, списочный)
    # физически становится колонкой CatalogRef.ЗначенияСвойствОбъектов — ему нужен ТОТ ЖЕ отбор
    # владельца, иначе лупа покажет значения всех свойств (потеря целостности, ради которой П2 и
    # делал отбор). Экстрактор гонит сведения через ту же ОписаниеРеквизита → у них есть
    # имяРеквизита/владелецЗначений. Объединяем реквизиты и списочные сведения в один набор формы.
    $списочные = @($в.реквизиты | Where-Object { $_.списочный }) +
        @($в.допСведения | Where-Object { $_.решение -eq 'реквизитОбъекта' -and $_.списочный })
    $formXml = ''
    $formMod = ''
    $defaultForm = ''
    $формыБлок = ''
    if ($списочные.Count -gt 0) {
        $поляФормы = @()
        $отборы = @()
        $idПоля = 10
        foreach ($р in $списочные) {
            $имяР = $р.имяРеквизита
            $поляФормы += ($FieldTpl.Replace('{{ИМЯ_РЕКВ}}', $имяР).
                Replace('{{ID}}', [string]$idПоля).Replace('{{ID2}}', [string]($idПоля + 1)).
                Replace('{{ID3}}', [string]($idПоля + 2))).TrimEnd()
            $idПоля += 3
            $отборы += "`tСвойство_$имяР = ПланыВидовХарактеристик.ДополнительныеРеквизитыИСведения.ПолучитьСсылку(Новый УникальныйИдентификатор(""$($р.владелецЗначений)""));"
            $отборы += "`tМассив_$имяР = Новый Массив;"
            $отборы += "`tМассив_$имяР.Добавить(Новый ПараметрВыбора(""Отбор.Владелец"", Свойство_$имяР));"
            $отборы += "`tЭлементы.$имяР.ПараметрыВыбора = Новый ФиксированныйМассив(Массив_$имяР);"
        }
        $formXml = $FormTpl.Replace('{{ИМЯ}}', $имя).Replace('{{ПОЛЯ_СПИСОЧНЫХ}}', ($поляФормы -join "`n"))
        $formMod = $FormModTpl.Replace('{{УСТАНОВКА_ОТБОРОВ}}', ($отборы -join "`n"))
        $uuidФормы = New-ДетерминированныйUuid "Пасьянс|Form|$видUuid|ФормаЭлемента"
        $defaultForm = "  <defaultObjectForm>Catalog.$имя.Form.ФормаЭлемента</defaultObjectForm>`n"
        $формыБлок = "`n  <forms uuid=""$uuidФормы"">`n    <name>ФормаЭлемента</name>`n    <synonym>`n      <key>ru</key>`n      <value>Форма элемента</value>`n    </synonym>`n    <usePurposes>PersonalComputer</usePurposes>`n  </forms>"
    }
    $mdo = $mdo.Replace('{{DEFAULT_OBJECT_FORM}}', $defaultForm)
    $mdo = $mdo.Replace('{{ФОРМЫ}}', $формыБлок)

    # модули: дескриптор — поля по дефолтныеПоля.тащить
    $поляДескриптора = @()
    foreach ($п in $в.дефолтныеПоля) {
        if ($п.тащить -and $п.имя -eq 'Организация') {
            $поляДескриптора += "`tДескрипторДоступа.Организация = ОбъектДоступа.Организация;"
        }
    }
    $mgr = $MgrTpl.Replace('{{ДЕСКРИПТОР_ПОЛЯ}}', ($поляДескриптора -join "`n"))
    $mgr = $mgr.Replace("`tДескрипторДоступа.ВидОбъекта = ОбъектДоступа.ВидДокумента;`n`n`nКонецПроцедуры",
        "`tДескрипторДоступа.ВидОбъекта = ОбъектДоступа.ВидДокумента;`n`nКонецПроцедуры")

    $каталог = Join-Path $Src "Catalogs\$имя"
    $отчёт.Add([pscustomobject]@{ вид = $имя; реквизитов = $блокиРеквизитов.Count
        каталог = $каталог; действие = $(if (Test-Path $каталог) {'перезапись'} else {'создание'}) })

    # UX (ОбработкаЗаполнения): подставляем uuid ВидаДокумента — новый элемент сразу знает свой вид
    $obj = $ObjTpl.Replace('{{ВИД_UUID}}', $видUuid)
    if ($Apply) {
        New-Item -ItemType Directory -Force $каталог | Out-Null
        Write-Текст "$каталог\$имя.mdo" $mdo $false
        Write-Текст "$каталог\ObjectModule.bsl" $obj $false
        Write-Текст "$каталог\ManagerModule.bsl" $mgr $false
        if ($списочные.Count -gt 0) {
            $каталогФормы = Join-Path $каталог 'Forms\ФормаЭлемента'
            New-Item -ItemType Directory -Force $каталогФормы | Out-Null
            Write-Текст "$каталогФормы\Form.form" $formXml $false
            Write-Текст "$каталогФормы\Module.bsl" $formMod $false
        }
    }

    # регистрация в Configuration.mdo (якорь — клин, наша секция)
    $cfgПуть = Join-Path $Src 'Configuration\Configuration.mdo'
    $cfg = Read-Текст $cfgПуть
    $регистрация = "  <catalogs>Catalog.$имя</catalogs>"
    if ($cfg.Текст.Contains($регистрация)) {
        $отчёт[-1] | Add-Member регистрация 'уже-была'
    } else {
        $якорь = '  <catalogs>Catalog.ПробныйДоговор</catalogs>'
        if (-not $cfg.Текст.Contains($якорь)) { throw "якорь регистрации (клин) не найден в Configuration.mdo" }
        if ($Apply) {
            Write-Текст $cfgПуть ($cfg.Текст.Replace($якорь, "$якорь`n$регистрация")) $cfg.BOM
        }
        $отчёт[-1] | Add-Member регистрация 'вставлена'
    }

    # ФИКС формы новых объектов (ошибка «Поле объекта не обнаружено (Справочник_<Имя>)»):
    # команда «Доп.Сведения» зовёт БСП ПолучитьОсновнойНаборСвойствДляОбъекта → имя набора =
    # СтрЗаменить("Справочник.<Имя>", ".", "_") = "Справочник_<Имя>" → предопределённый элемент
    # НаборыДополнительныхРеквизитовИСведений[имя]; у сгенерённого вида его НЕТ → рантайм-падение.
    # Врезаем пустой ЛИСТОВОЙ предопределённый набор перед </predefined> (сведения приземлены в
    # колонки, набор пуст — команда просто открывается без ошибки; детерминированный id, идемпотентно).
    $наборыПуть = Join-Path $Src 'Catalogs\НаборыДополнительныхРеквизитовИСведений\НаборыДополнительныхРеквизитовИСведений.mdo'
    $имяНабора = "Справочник_$имя"
    $наборы = Read-Текст $наборыПуть
    if ($наборы.Текст.Contains("<name>$имяНабора</name>")) {
        $отчёт[-1] | Add-Member наборСвойств 'уже-был'
    } else {
        $idНабора = New-ДетерминированныйUuid "Пасьянс|НаборСвойств|$видUuid"
        $синонимЭск = ($в.справочник.синоним -replace '&', '&amp;' -replace '<', '&lt;' -replace '>', '&gt;' -replace '"', '&quot;')
        $блокНабора = @(
            "    <items id=""$idНабора"">"
            "      <name>$имяНабора</name>"
            "      <description>Доп. свойства: $синонимЭск (Пасьянизатор)</description>"
            "      <code xsi:type=""core:StringValue"">"
            "        <value></value>"
            "      </code>"
            "    </items>"
        ) -join "`n"
        $якорьН = '  </predefined>'
        if (-not $наборы.Текст.Contains($якорьН)) { throw "якорь </predefined> не найден в НаборыДополнительныхРеквизитовИСведений.mdo" }
        if ($Apply) {
            Write-Текст $наборыПуть ($наборы.Текст.Replace($якорьН, "$блокНабора`n$якорьН")) $наборы.BOM
        }
        $отчёт[-1] | Add-Member наборСвойств 'вставлен'
    }
}

$отчёт | Format-Table -AutoSize | Out-String | Write-Host
if (-not $Apply) { Write-Host 'DRY-RUN: файлы не писались (добавь -Apply)' -ForegroundColor Yellow }
if ($отчёт.Count -eq 0) { Write-Host 'Ни один вид не отобран'; exit 2 }
exit 0
