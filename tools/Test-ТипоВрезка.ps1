# Test-ТипоВрезка.ps1 — гейт Т конвейера (такт 4, П3.1): инверсия Типозамкнутости —
# «тип КАЖДОГО сгенерённого вида ПРИСУТСТВУЕТ во всех обязательных составах ядра».
# Формат-мишень такта 4 — EDT-src (те же три вкуса якоря, что у Find-ЯкорныеТочки/врезки);
# MDClasses-вариант (прямая Типозамкнутость по банкТипов) — G5 такта 6, там экспорт и так есть.
# Отложено ЯВНО (владельцы): ТЧ/predefined EAV-хвоста — такт 5; скан форм на составные
# data-path (класс предохранителя №4) — такт 5.
# Чек-лист: матрица вид×точка (ВРЕЗАНО/ИСКЛЮЧЕНО=skip/ПРОПУЩЕНО=FAIL); маркер-блоки в ОБЕИХ
# .bsl-точках; полнота Пасьянс_Типы; обвязка модулей вида; ПараметрыВыбора у списочных.
# Коды: 0 = зелено, 2 = красно.

[CmdletBinding()]
param(
    [string]$Src = 'E:\1CRoot\EDTPasyanizator\До_КОРП_30_0407\src',
    [string]$КартаВрезок = "$PSScriptRoot\КартаВрезок.tsv",
    [string]$Карта,           # JSON-карта: включает проверку списочных у сгенерённых видов
    [string[]]$Виды           # по умолчанию — скан src/Catalogs по маркеру Пасьянизатора
)

$ErrorActionPreference = 'Stop'
. "$PSScriptRoot\Пасьянс-Общее.ps1"
$ошибки = [System.Collections.Generic.List[string]]::new()
function Fail([string]$м) { $script:ошибки.Add($м); Write-Host "  FAIL $м" -ForegroundColor Red }
function Ok([string]$м) { Write-Host "  ok   $м" -ForegroundColor Green }

Write-Host '=== Test-ТипоВрезка (гейт Т) ==='

# --- виды под проверку ---
if (-not $Виды) {
    $Виды = @(Get-ChildItem (Join-Path $Src 'Catalogs') -Directory | Where-Object {
        $mdo = Join-Path $_.FullName "$($_.Name).mdo"
        (Test-Path $mdo) -and ([System.IO.File]::ReadAllText($mdo).Contains('Пасьянизатор из вида'))
    } | ForEach-Object Name)
}
if (-not $Виды) { Fail 'ни одного сгенерённого вида в конфе'; exit 2 }
Write-Host "  виды: $($Виды -join ', ')"

# --- 1. матрица вид × точка по КартаВрезок.tsv (по-блочно, как врезка) ---
$Якоря = @{
    'types-ref'    = '<types>CatalogRef.ДокументыПредприятия</types>'
    'types-object' = '<types>CatalogObject.ДокументыПредприятия</types>'
    'owners'       = '<owners>Catalog.ДокументыПредприятия</owners>'
}
function Вставка([string]$Вкус, [string]$Вид) {
    switch ($Вкус) {
        'types-ref'    { "<types>CatalogRef.$Вид</types>" }
        'types-object' { "<types>CatalogObject.$Вид</types>" }
        'owners'       { "<owners>Catalog.$Вид</owners>" }
    }
}
$всеТочки = Import-Csv $КартаВрезок -Delimiter "`t"
# Неопознанный статус НЕ должен молча выпадать из-под гейта (опечатка 'inserted'/хвостовой пробел).
$знакомые = 'insert', 'verified', 'skip'
foreach ($т in $всеТочки) {
    if ($т.статус -notin $знакомые) { Fail "КартаВрезок: строка $($т.файл) [$($т.вкус)] с неизвестным статусом '$($т.статус)' (ожидалось $($знакомые -join '/'))" }
}
$точки = @($всеТочки | Where-Object { $_.статус -in 'insert', 'verified' })
$исключено = @($всеТочки | Where-Object { $_.статус -eq 'skip' }).Count
$врезано = 0
$пропущено = 0
foreach ($точка in $точки) {
    $путь = Join-Path $Src $точка.файл
    if (-not (Test-Path $путь)) { Fail "точка $($точка.файл): файла нет"; continue }
    $строки = (Read-Текст $путь).Текст -split "`r?`n"
    $якорь = $Якоря[$точка.вкус]
    # CRITICAL (пре-ревью такта 4): если якорной строки в файле НЕТ, цикл ниже не выполнит ни
    # одного ассерта — точка молча выпадала из матрицы, гейт зеленел без проверки. Считаем якоря.
    $якорейНайдено = 0
    for ($i = 0; $i -lt $строки.Count; $i++) {
        if (-not $строки[$i].Contains($якорь)) { continue }
        $якорейНайдено++
        $конецБлока = $строки.Count - 1
        if ($точка.вкус -ne 'owners') {
            for ($j = $i + 1; $j -lt $строки.Count; $j++) {
                if ($строки[$j].Contains('</type>')) { $конецБлока = $j; break }
            }
        }
        $начало = $(if ($точка.вкус -eq 'owners') { 0 } else { $i })
        foreach ($вид in $Виды) {
            $нужнаяВставка = Вставка $точка.вкус $вид
            $есть = $false
            for ($j = $начало; $j -le $конецБлока; $j++) {
                if ($строки[$j].Contains($нужнаяВставка)) { $есть = $true; break }
            }
            if ($есть) { $врезано++ }
            else {
                $пропущено++
                Fail "ПРОПУЩЕНО: $вид в $($точка.файл) [$($точка.вкус)] (блок у строки $($i + 1))"
            }
        }
        if ($точка.вкус -eq 'owners') { break }  # owners проверяется по всему файлу один раз
    }
    if ($якорейНайдено -eq 0) {
        Fail "ЯКОРЬ НЕ НАЙДЕН: '$якорь' в $($точка.файл) [$($точка.вкус)] — точка врезки не проверена (гейт был бы слеп)"
    }
}
if ($пропущено -eq 0 -and -not ($ошибки | Where-Object { $_ -like 'ЯКОРЬ НЕ НАЙДЕН*' })) {
    Ok "матрица вид×точка: врезано $врезано, исключено (skip) $исключено, пропущено 0"
}

# --- 2. маркер-блоки в ОБЕИХ .bsl-точках ---
foreach ($блок in @(
        @{Файл = 'CommonModules\ДействияСервер\Module.bsl'; Метка = 'ВидОбъекта' },
        @{Файл = 'Enums\ТипыПриложенийЗадач\ManagerModule.bsl'; Метка = 'ТипПриложения' })) {
    $текст = (Read-Текст (Join-Path $Src $блок.Файл)).Текст
    $целый = $текст.Contains("ПАСЬЯНС-ВРЕЗКА:$($блок.Метка) НАЧАЛО") -and
             $текст.Contains("ПАСЬЯНС-ВРЕЗКА:$($блок.Метка) КОНЕЦ") -and
             $текст.Contains('Пасьянс_Типы.ЭтоВидПасьянса')
    if ($целый) { Ok "маркер-блок $($блок.Метка): на месте, зовёт Пасьянс_Типы" }
    else { Fail "маркер-блок $($блок.Метка) повреждён/отсутствует в $($блок.Файл)" }
}

# --- 3. полнота Пасьянс_Типы против видов конфы ---
$модульТипов = (Read-Текст (Join-Path $Src 'CommonModules\Пасьянс_Типы\Module.bsl')).Текст
foreach ($вид in $Виды) {
    if ($модульТипов.Contains("Тип(""СправочникСсылка.$вид"")")) { Ok "Пасьянс_Типы знает $вид" }
    else { Fail "Пасьянс_Типы НЕ содержит $вид — ядро не опознает вид (цепочка ВидОбъекта)" }
}

# --- 4. обвязка модулей вида: дескрипторы + классификатор ---
foreach ($вид in $Виды) {
    $база = Join-Path $Src "Catalogs\$вид"
    $объектный = (Read-Текст "$база\ObjectModule.bsl").Текст
    $менеджера = (Read-Текст "$база\ManagerModule.bsl").Текст
    $mdo = (Read-Текст "$база\$вид.mdo").Текст
    if (-not $объектный.Contains('ОпределитьДескрипторыОбъекта')) { Fail "${вид}: ObjectModule без ОпределитьДескрипторыОбъекта (RLS-дескрипторы не пишутся)" }
    if (-not $менеджера.Contains('ЗаполнитьОсновнойДескриптор')) { Fail "${вид}: ManagerModule без ЗаполнитьОсновнойДескриптор" }
    if (-not $mdo.Contains('<name>ВидДокумента</name>')) { Fail "${вид}: .mdo без реквизита-классификатора ВидДокумента" }
}
if (-not ($ошибки | Where-Object { $_ -like '*Module без*' -or $_ -like '*без реквизита*' })) { Ok 'обвязка модулей видов (дескрипторы + ВидДокумента) на месте' }

# --- 5. списочные: сгенерённый вид со списочным реквизитом обязан нести FK на общий справочник ---
# НАХОДКА П2 такта 5 (раскопка заметки деда №1): фиксированный Отбор.Владелец у ПРИЗЕМЛЁННОЙ
# колонки в .mdo НЕВОЗМОЖЕН — живой EAV-отбор это choiceParameterLinks (динамический, владелец из
# СОСЕДНЕГО реквизита Свойство в строке ТЧ; у колонки соседа нет), а фиксированный choiceParameters
# с ReferenceValue ссылается на конкретный элемент ТОЛЬКО если он ПРЕДОПРЕДЕЛЁННЫЙ; владелец-ПВХ =
# обычные данные → сослаться нельзя. Вариант А (FK на общий Catalog.ЗначенияСвойствОбъектов) отбор
# не несёт по конструкции. Проверяем КОРРЕКТНОСТЬ ТИПА ссылки, а не невозможный отбор.
# (Границу блока держим лукахедом; MDClasses-Типозамкнутость по банкТипов заменит текст в такте 6.)
if ($Карта) {
    $к = Read-ПасьянсКарта $Карта
    $проверено = 0
    foreach ($в in $к.виды) {
        $имя = $в.справочник.имя
        if ($имя -notin $Виды) { continue }
        $mdo = (Read-Текст (Join-Path $Src "Catalogs\$имя\$имя.mdo")).Текст
        $путьФормы = Join-Path $Src "Catalogs\$имя\Forms\ФормаЭлемента\Module.bsl"
        $модульФормы = if (Test-Path $путьФормы) { (Read-Текст $путьФормы).Текст } else { '' }
        # такт 5 П4 (пре-ревью D3): приземлённое СПИСОЧНОЕ сведение (решение=реквизитОбъекта) —
        # та же FK-колонка CatalogRef.ЗначенияСвойствОбъектов, что и списочный реквизит, и обязана
        # нести тот же отбор владельца. Раньше гейт итерировал только реквизиты → списочное сведение
        # приземлялось колонкой БЕЗ отбора и молча зеленело (слепая зона).
        $списочныеВида = @($в.реквизиты | Where-Object { $_.списочный }) +
            @($в.допСведения | Where-Object { $_.решение -eq 'реквизитОбъекта' -and $_.списочный })
        foreach ($р in $списочныеВида) {
            $проверено++
            $escИмя = [regex]::Escape($р.имяРеквизита)
            if (-not ($mdo -match "(?s)<name>$escИмя</name>((?!</attributes>).)*?CatalogRef\.ЗначенияСвойствОбъектов")) {
                Fail "$имя.$($р.имяРеквизита): списочный без FK-типа CatalogRef.ЗначенияСвойствОбъектов"
            }
            # ПАРНОСТЬ (пре-ревью D3): FK на ГЛОБАЛЬНЫЙ Catalog.ЗначенияСвойствОбъектов без программного
            # отбора владельца = лупа покажет значения ВСЕХ свойств всех видов (потеря целостности).
            if (-not ($модульФормы -match "Элементы\.$escИмя\.ПараметрыВыбора")) {
                Fail "$имя.$($р.имяРеквизита): списочная FK-колонка без ПараметрыВыбора('Отбор.Владелец') в форме (лупа покажет чужие значения)"
            }
        }
    }
    Ok "списочных колонок проверено (FK-тип + отбор владельца): $проверено"
}

Write-Host ''
if ($ошибки.Count -eq 0) {
    Write-Host "=== ВЕРДИКТ ГЕЙТА Т: ЗЕЛЕНО (видов $($Виды.Count), врезок $врезано) ===" -ForegroundColor Green
    exit 0
} else {
    Write-Host "=== ВЕРДИКТ ГЕЙТА Т: КРАСНО, проблем: $($ошибки.Count) ===" -ForegroundColor Red
    exit 2
}
