# --- 2. маркер-блоки ---
function Установить-МаркерБлок([string]$Путь, [string]$Метка, [string]$ЯкорьПеред, [string]$Тело) {
    $ф = Read-Текст $Путь
    $начало = "// ПАСЬЯНС-ВРЕЗКА:$Метка НАЧАЛО (генерится New-БслПредохранители.ps1, руками не править)"
    $конец = "// ПАСЬЯНС-ВРЕЗКА:$Метка КОНЕЦ"
    $rx = [regex]::new("(?s)[ \t]*// ПАСЬЯНС-ВРЕЗКА:$([regex]::Escape($Метка)) НАЧАЛО.*?// ПАСЬЯНС-ВРЕЗКА:$([regex]::Escape($Метка)) КОНЕЦ\r?\n")
    $текст = $rx.Replace($ф.Текст, '')
    $позиция = $текст.IndexOf($ЯкорьПеред)
    if ($позиция -lt 0) { throw "якорь для блока $Метка не найден в $Путь" }
    $отступ = ''
    $н = $текст.LastIndexOf("`n", $позиция)
    if ($н -ge 0) { $отступ = $текст.Substring($н + 1, $позиция - $н - 1) }
    $блок = "$отступ$начало`n$Тело`n$отступ$конец`n"
    $текст = $текст.Insert($позиция - $отступ.Length, $блок)
    if ($Apply) { Write-Текст $Путь $текст $ф.BOM }
    Write-Host "Маркер-блок ${Метка}: установлен (якорь найден)"
}

