# Test-ПасьянсПрогон.ps1 — терминальный гейт П конвейера: машиночитаемый вердикт смоука.
# Парсит лог обработки ПасьянизаторСмоук (E:\1CRoot\Пасьянизатор-прогон-<RunId>.log):
#   [ПАСЬЯНС] <время> PASS|FAIL|INFO <шаг> <вид> <детали>
# Правила зелёного: RunId совпал В ИМЕНИ ФАЙЛА И в строке START внутри лога, START ровно один
# (подложный/вчерашний/дописанный лог = красно); для КАЖДОГО ожидаемого вида полный набор
# PASS-маркеров шагов 1-6 (отсутствие маркера = красно); 0 FAIL; ВЕРДИКТ-строка присутствует;
# 0 сигнатур предохранителей. -ТребоватьЗавершение (такт 5+) дополнительно требует
# «7.Итог ... Завершен=Да, РезультатСогласования=Согласовано». Коды: 0 = зелено, 2 = красно.

[CmdletBinding()]
param(
    [Parameter(Mandatory)] [string]$RunId,
    [string[]]$ОжидаемыеВиды,
    [string]$КаталогЛогов = 'E:\1CRoot',
    # такт 5+: требовать полный финиш процесса (Завершен=Да + РезультатСогласования=Согласовано)
    [switch]$ТребоватьЗавершение,
    # гейт Р: требовать PASS-шаги регресса родного ДокументыПредприятия (боевой конвейер — всегда)
    [switch]$ТребоватьРегресс
)

$ErrorActionPreference = 'Stop'
$лог = Join-Path $КаталогЛогов "Пасьянизатор-прогон-$RunId.log"
$ошибки = [System.Collections.Generic.List[string]]::new()
function Fail([string]$м) { $script:ошибки.Add($м); Write-Host "  FAIL $м" -ForegroundColor Red }
function Ok([string]$м) { Write-Host "  ok   $м" -ForegroundColor Green }

Write-Host "=== Test-ПасьянсПрогон: RunId=$RunId ==="
if (-not (Test-Path $лог)) { Fail "лог не найден: $лог"; exit 2 }

$строки = Get-Content $лог -Encoding utf8 | Where-Object { $_ -match '\[ПАСЬЯНС\]' }
if (-not $строки) { Fail 'в логе нет маркеров [ПАСЬЯНС]'; exit 2 }

# RunId сверяется и ВНУТРИ лога, не только в имени файла: переименованный/чужой лог — красно.
# Двойной START = два прогона дописались в один файл — маркеры вперемешку, верить нельзя.
$старты = @($строки | Where-Object { $_ -match '\sINFO\sSTART\s' })
if ($старты.Count -ne 1) { Fail "в логе $($старты.Count) строк START (ожидается ровно 1 прогон на файл)" }
elseif ($старты[0] -notmatch "RunId=$([regex]::Escape($RunId))(\s|$)") {
    Fail "RunId внутри лога не совпадает с запрошенным: $($старты[0].Trim())"
} else { Ok "RunId подтверждён внутри лога: $RunId" }

# 0 FAIL
$фейлы = @($строки | Where-Object { $_ -match '\sFAIL\s' })
if ($фейлы.Count -gt 0) {
    $фейлы | ForEach-Object { Fail $_ }
} else { Ok 'FAIL-маркеров нет' }

# ВЕРДИКТ присутствует и зелёный
$вердикт = @($строки | Where-Object { $_ -match 'ВЕРДИКТ' }) | Select-Object -Last 1
if (-not $вердикт) { Fail 'нет строки ВЕРДИКТ (прогон не дошёл до конца)' }
elseif ($вердикт -notmatch 'ЗЕЛЕНО') { Fail "вердикт обработки: $вердикт" }
else { Ok $вердикт.Trim() }

# обязательное ядро гейта: генерация -> старт -> задача -> выполнение движком
# (7.Итог — целевой INFO, полнота маршрута, в must не входит)
$шаги = @('1.Элемент', '2.Дескриптор', '3.Старт', '5.Задача', '6.Задачи')
if (-not $ОжидаемыеВиды) {
    # вид — regex-группой, НЕ позиционным сплитом: время «yyyy-MM-dd HH:mm:ss» занимает два
    # токена, и [4] попадал на имя шага (ловилось живым прогоном: «виды из лога: 1.Элемент»)
    $ОжидаемыеВиды = @($строки | ForEach-Object {
            if ($_ -match '\sPASS\s1\.Элемент\s(\S+)') { $Matches[1] }
        } | Sort-Object -Unique)
    Write-Host "  (виды из лога: $($ОжидаемыеВиды -join ', ')) — авторежим не видит виды, молча выпавшие из прогона; для строгой сверки передавай -ОжидаемыеВиды"
}
if (-not $ОжидаемыеВиды) { Fail 'не удалось определить ни одного вида' }
foreach ($вид in $ОжидаемыеВиды) {
    foreach ($шаг in $шаги) {
        $есть = $строки | Where-Object { $_ -match "\sPASS\s$([regex]::Escape($шаг))\s$([regex]::Escape($вид))(\s|$)" }
        if (-not $есть) { Fail "${вид}: нет PASS-маркера шага $шаг" }
    }
}
if ($ошибки.Count -eq 0) { Ok "все шаги PASS для видов: $($ОжидаемыеВиды -join ', ')" }

# сверка счётчика вердикта с ожиданием (ревью П4): ловит вид, упавший ДО «1.Элемент», и
# молча выпавший вид при ПУСТОМ файле-флаге (скан всех — заказ-контроль смоука не работает);
# на боевом конвейере -ОжидаемыеВиды передавать ЯВНО, авторежим — только для ручной отладки
$заявленоВидов = if ($вердикт -and $вердикт -match 'видов=(\d+)') { [int]$Matches[1] } else { -1 }
if ($заявленоВидов -ne @($ОжидаемыеВиды).Count) {
    Fail "вердикт рапортует видов=$заявленоВидов, гейт ожидает $(@($ОжидаемыеВиды).Count) ($($ОжидаемыеВиды -join ', '))"
} else { Ok "счётчик видов вердикта совпал: $заявленоВидов" }

# гейт Р: регресс родного вида — ядро Р-шагов PASS (FAIL и так красит, этот блок ловит
# ОТСУТСТВИЕ регресс-прогона в логе). Р.Задачи — INFO-диагностика: демо-правила маршрутизации
# ДП не рождают ЗадачаИсполнителя программным стартом (разбор — такт 5)
if ($ТребоватьРегресс) {
    foreach ($шагР in @('Р.Предмет', 'Р.Старт', 'Р.Задача')) {
        if (-not ($строки | Where-Object { $_ -match "\sPASS\s$([regex]::Escape($шагР))\s" })) {
            Fail "регресс родного: нет PASS-маркера шага $шагР"
        }
    }
    if ($ошибки.Count -eq 0) { Ok 'регресс родного ДокументыПредприятия подтверждён (Р-шаги)' }
}

# такт 5+: полный финиш процесса по 7.Итог (пока смоук пишет его как INFO — включается ключом)
if ($ТребоватьЗавершение) {
    foreach ($вид in $ОжидаемыеВиды) {
        # РезультатСогласования — последний токен строки; якорь конца отсекает «Согласовано с
        # замечаниями» (иначе подстрочный матч засчитывал частичное согласование как полный финиш).
        $итогВида = $строки | Where-Object {
            $_ -match "\sINFO\s7\.Итог\s$([regex]::Escape($вид))\s.*Завершен=Да.*РезультатСогласования=Согласовано\s*$" }
        if (-not $итогВида) { Fail "${вид}: нет 7.Итог с Завершен=Да и РезультатСогласования=Согласовано (ровно, не «с замечаниями»)" }
    }
    if ($ошибки.Count -eq 0) { Ok 'финиш процессов подтверждён (7.Итог)' }
}

# сигнатуры предохранителей
$сигнатуры = 'Неизвестный вид объекта|Неизвестный тип приложения|Неверный тип значения|не обнаружено \('
$пробои = @($строки | Where-Object { $_ -match $сигнатуры })
if ($пробои.Count -gt 0) { $пробои | ForEach-Object { Fail "сигнатура предохранителя: $_" } }
else { Ok 'сигнатур предохранителей нет' }

Write-Host ''
if ($ошибки.Count -eq 0) {
    Write-Host "=== ВЕРДИКТ ГЕЙТА П: ЗЕЛЕНО ===" -ForegroundColor Green
    exit 0
} else {
    Write-Host "=== ВЕРДИКТ ГЕЙТА П: КРАСНО, проблем: $($ошибки.Count) ===" -ForegroundColor Red
    exit 2
}
