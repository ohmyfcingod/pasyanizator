# Test-Карта.ps1 — гейт G0 конвейера Пасьянса: валидация JSON-карты до любой генерации.
# Проверки: схема (ПасьянсКарта.schema.json) → счётный гейт «итоги» → уникальность и
# стоп-листы имён → коллизии с метаданными конфы (свои объекты опознаются по комментарию
# «Пасьянизатор из вида <uuid>») → банкТипов резолвится в исходниках → типы покрыты
# ТипыРеквизитов.json → квалификаторы обязательны для Число/Строка.
# Коды возврата: 0 = зелено, 2 = красно (соглашение Test-Типозамкнутость). Требует pwsh 7.

[CmdletBinding()]
param(
    [Parameter(Mandatory)] [string]$Карта,
    [string]$Src = 'E:\1CRoot\EDTPasyanizator\До_КОРП_30_0407\src',
    [string]$Схема = "$PSScriptRoot\ПасьянсКарта.schema.json",
    [string]$ТипыРеквизитов = "$PSScriptRoot\Шаблоны\ТипыРеквизитов.json",
    # фактический состав прогона генератора (-Виды оркестратора): межвидовая ссылка на вид
    # карты ВНЕ этого списка = битая CatalogRef до самого EDT-этапа (ревью П3)
    [string[]]$ВидыПрогона
)

$ErrorActionPreference = 'Stop'
$ошибки = [System.Collections.Generic.List[string]]::new()
function Fail([string]$м) { $script:ошибки.Add($м); Write-Host "  FAIL $м" -ForegroundColor Red }
function Ok([string]$м) { Write-Host "  ok   $м" -ForegroundColor Green }

# Стоп-листы имён (уроки: стандартные реквизиты + встроенные свойства формы, грабля «Параметры»)
$СтопРеквизиты = @('Код','Наименование','Ссылка','ПометкаУдаления','Родитель','Владелец',
    'ЭтоГруппа','Предопределенный','ИмяПредопределенныхДанных','ВидДокумента',
    'Параметры','Модифицированность','ЭтотОбъект','ДополнительныеСвойства')

Write-Host "=== Test-Карта: $Карта ==="

# 1. Парс + схема
$текст = Get-Content $Карта -Raw -Encoding utf8
try { $к = $текст | ConvertFrom-Json -Depth 20 } catch { Fail "JSON не парсится: $_"; exit 2 }
# строгая политика версий (ревью П1): генератор объявляет РОВНО поддерживаемую версию схемы;
# карта новее = молча потерянные новые блоки, карта старее = недозаполненный контракт — оба красно
$ПоддерживаемаяСхема = '1.2'
if ($к.schemaVersion -ne $ПоддерживаемаяСхема) {
    Fail "schemaVersion '$($к.schemaVersion)' ≠ поддерживаемой генератором '$ПоддерживаемаяСхема' — обнови генератор (карта новее) или пересними карту (старее)"
}
$схемаОк = Test-Json -Json $текст -SchemaFile $Схема -ErrorAction SilentlyContinue -ErrorVariable схемаОшибки
if ($схемаОк) { Ok "схема v$ПоддерживаемаяСхема" } else { $схемаОшибки | ForEach-Object { Fail "схема: $($_.Exception.Message)" } }

# 2. Счётный гейт «итоги»
$видов = @($к.виды).Count
$тащим = (@($к.виды) | ForEach-Object { @($_.реквизиты).Count } | Measure-Object -Sum).Sum
$вEAV  = (@($к.виды) | ForEach-Object { @($_.остаютсяВEAV).Count } | Measure-Object -Sum).Sum
if ($к.итоги.видовВыбрано -ne $видов) { Fail "итоги.видовВыбрано=$($к.итоги.видовВыбрано), фактически $видов" } else { Ok "итоги: видов=$видов" }
if ($к.итоги.реквизитовТащим -ne $тащим) { Fail "итоги.реквизитовТащим=$($к.итоги.реквизитовТащим), фактически $тащим" } else { Ok "итоги: реквизитов=$тащим" }
if ($к.итоги.реквизитовВEAV -ne $вEAV) { Fail "итоги.реквизитовВEAV=$($к.итоги.реквизитовВEAV), фактически $вEAV" } else { Ok "итоги: вEAV=$вEAV" }
# такт 5 П4: счётный гейт по доп.сведениям. Всего = все; Тащим = решение реквизитОбъекта
# (приземляется физ.колонкой); ВРС = решение остаётсяВРС (хвост в независимом РС).
# @(...).Count и [int] страхуют пустой массив (Measure-Object -Sum от пусто = $null).
$сведВсего = [int]((@($к.виды) | ForEach-Object { @($_.допСведения).Count } | Measure-Object -Sum).Sum)
$сведТащим = [int]((@($к.виды) | ForEach-Object { @($_.допСведения | Where-Object { $_.решение -eq 'реквизитОбъекта' }).Count } | Measure-Object -Sum).Sum)
$сведВРС   = [int]((@($к.виды) | ForEach-Object { @($_.допСведения | Where-Object { $_.решение -eq 'остаётсяВРС' }).Count } | Measure-Object -Sum).Sum)
if ($сведТащим + $сведВРС -ne $сведВсего) { Fail "решения сведений: тащим=$сведТащим + вРС=$сведВРС ≠ всего=$сведВсего (пустое/чужое решение)" } else { Ok "сведения: решения покрывают все $сведВсего" }
if ($null -ne $к.итоги.сведенийВсего -and $к.итоги.сведенийВсего -ne $сведВсего) { Fail "итоги.сведенийВсего=$($к.итоги.сведенийВсего), фактически $сведВсего" } else { Ok "итоги: сведенийВсего=$сведВсего" }
if ($null -ne $к.итоги.сведенийТащим -and $к.итоги.сведенийТащим -ne $сведТащим) { Fail "итоги.сведенийТащим=$($к.итоги.сведенийТащим), фактически $сведТащим" } else { Ok "итоги: сведенийТащим=$сведТащим" }
if ($null -ne $к.итоги.сведенийВРС -and $к.итоги.сведенийВРС -ne $сведВРС) { Fail "итоги.сведенийВРС=$($к.итоги.сведенийВРС), фактически $сведВРС" } else { Ok "итоги: сведенийВРС=$сведВРС" }
# П1.5 (пре-ревью #28): полный счётный гейт — предупреждения и коллизии тоже сверяются.
# @($null).Count=1 — грабля PS: свойство без значения фильтруем ДО оборачивания в массив
$предупрежденийФакт = @($к.предупрежденияВыбора | Where-Object { $_ }).Count
@($к.виды) | ForEach-Object { @($_.реквизиты) | Where-Object { $_ -and $_.предупреждения } |
    ForEach-Object { $предупрежденийФакт += @($_.предупреждения).Count } }
# Прореженный реквизит уносит свои предупреждения в остаютсяВEAV — считаем и их, иначе
# итоги.предупреждений (по ВСЕМ реквизитам до прореживания) > факта → ложное КРАСНО (пре-ревью такта 4).
@($к.виды) | ForEach-Object { @($_.остаютсяВEAV) | Where-Object { $_ -and $_.предупреждения } |
    ForEach-Object { $предупрежденийФакт += @($_.предупреждения).Count } }
# такт 5 П4 (пре-ревью D1): сведения проходят через ту же ОписаниеРеквизита, что и реквизиты —
# списочное/составное сведение порождает предупреждение, которое экстрактор кладёт и в
# итоги.предупреждений, и в допСведения[].предупреждения. Без симметричного прохода факт < итога →
# ложное КРАСНО на валидной карте со списочным сведением.
@($к.виды) | ForEach-Object { @($_.допСведения) | Where-Object { $_ -and $_.предупреждения } |
    ForEach-Object { $предупрежденийФакт += @($_.предупреждения).Count } }
if ($null -ne $к.итоги.предупреждений -and $к.итоги.предупреждений -ne $предупрежденийФакт) {
    Fail "итоги.предупреждений=$($к.итоги.предупреждений), фактически $предупрежденийФакт"
} else { Ok "итоги: предупреждений=$предупрежденийФакт" }
$коллизийФакт = @($к.коллизии | Where-Object { $_ }).Count
if ($null -ne $к.итоги.коллизийРазрешено -and $к.итоги.коллизийРазрешено -ne $коллизийФакт) {
    Fail "итоги.коллизийРазрешено=$($к.итоги.коллизийРазрешено), фактически $коллизийФакт"
} else { Ok "итоги: коллизий=$коллизийФакт" }

# 3. Имена: уникальность справочников, стоп-листы реквизитов, дедуп внутри вида
$именаСправочников = @($к.виды | ForEach-Object { $_.справочник.имя })
$дубли = $именаСправочников | Group-Object | Where-Object Count -gt 1
if ($дубли) { $дубли | ForEach-Object { Fail "дубль имени справочника в карте: $($_.Name)" } } else { Ok 'имена справочников уникальны' }
foreach ($в in $к.виды) {
    $имена = @($в.реквизиты | ForEach-Object { $_.имяРеквизита })
    $имена | Group-Object | Where-Object Count -gt 1 | ForEach-Object {
        Fail "$($в.справочник.имя): дубль реквизита $($_.Name)" }
    $имена | Where-Object { $_ -in $СтопРеквизиты } | ForEach-Object {
        Fail "$($в.справочник.имя): реквизит '$_' в стоп-листе (стандартный/встроенный)" }
    # Коллизия с дефолтным полем (Организация/Сумма и т.п.): генератор рендерит их первым
    # проходом отдельным <attributes> — одноимённый реквизит даёт ДВА <name> в .mdo, падение
    # только на EDT-этапе. Экстрактор страхует себя стоп-листом, но G0 — контракт для любой карты.
    $дефИмена = @($в.дефолтныеПоля | Where-Object { $_.тащить } | ForEach-Object { $_.имя })
    $имена | Where-Object { $_ -in $дефИмена } | ForEach-Object {
        Fail "$($в.справочник.имя): реквизит '$_' конфликтует с одноимённым дефолтным полем (дубль <attributes> в .mdo)" }
    # uuid реквизита сеется от «свойство»: дубль свойства внутри вида = два attributes с одним uuid
    @($в.реквизиты | ForEach-Object { $_.свойство }) | Group-Object | Where-Object Count -gt 1 |
        ForEach-Object { Fail "$($в.справочник.имя): свойство $($_.Name) затащено двумя реквизитами — дубль uuid" }
}

# 4. Коллизии с метаданными конфы (свои — переиспользуются, критика №19)
$видUuid = @{}
$к.виды | ForEach-Object { $видUuid[$_.справочник.имя] = $_.вид.uuid }
$ветки = @('Catalogs','Documents','Enums','ChartsOfCharacteristicTypes','BusinessProcesses','Tasks')
foreach ($в in $к.виды) {
    $имя = $в.справочник.имя
    foreach ($ветка in $ветки) {
        $mdo = Join-Path $Src "$ветка\$имя\$имя.mdo"
        if (Test-Path $mdo) {
            $содержимое = [System.IO.File]::ReadAllText($mdo)
            if ($ветка -eq 'Catalogs' -and $содержимое.Contains("Пасьянизатор из вида $($видUuid[$имя])")) {
                Ok "$имя уже в конфе — СВОЙ (тот же вид), прогон = обновление"
            } else {
                Fail "коллизия: $ветка.$имя уже существует в конфе и это не наш объект"
            }
        }
    }
}
if (-not ($ошибки | Where-Object { $_ -like 'коллизия*' })) { Ok 'коллизий имён с конфой нет' }

# 5. банкТипов и ссылочные типы реквизитов резолвятся в исходниках
$веткаПоПрефиксу = @{
    'CatalogRef' = 'Catalogs'; 'DocumentRef' = 'Documents'; 'EnumRef' = 'Enums'
    'ChartOfCharacteristicTypesRef' = 'ChartsOfCharacteristicTypes'
    'Characteristic' = 'ChartsOfCharacteristicTypes'
    'DefinedType' = 'DefinedTypes'; 'BusinessProcessRef' = 'BusinessProcesses'; 'TaskRef' = 'Tasks'
}
$всеТипы = [System.Collections.Generic.HashSet[string]]::new()
@($к.банкТипов) | ForEach-Object { [void]$всеТипы.Add($_) }
$к.виды | ForEach-Object { $_.реквизиты | ForEach-Object { $_.типы | Where-Object ссылочныйТип |
    ForEach-Object { [void]$всеТипы.Add($_.ссылочныйТип) } } }
foreach ($т in $всеТипы) {
    $префикс, $объект = $т -split '\.', 2
    $ветка = $веткаПоПрефиксу[$префикс]
    if (-not $ветка) { Fail "банкТипов: неизвестный префикс '$т'"; continue }
    if ($именаСправочников -contains $объект -and $префикс -eq 'CatalogRef') {
        if ($ВидыПрогона -and $объект -notin $ВидыПрогона -and -not (Test-Path (Join-Path $Src "Catalogs\$объект\$объект.mdo"))) {
            # вид есть в карте, но не входит в текущий прогон и ещё не существует в конфе
            Fail "межвидовая ссылка: $т указывает на вид карты '$объект' вне текущего прогона (-Виды) — битая ссылка до EDT-этапа"
        } else { Ok "$т — будущий справочник карты" }
        continue
    }
    # именно <Имя>.mdo, не каталог: пустой каталог-огрызок (git-артефакт) — не объект конфы
    if (-not (Test-Path (Join-Path $Src "$ветка\$объект\$объект.mdo"))) { Fail "банкТипов: $т не найден в конфе ($ветка\$объект\$объект.mdo)" }
}
Ok "типов проверено: $($всеТипы.Count)"

# 6. Виды типов покрыты шаблонами + квалификаторы на месте
$тр = Get-Content $ТипыРеквизитов -Raw -Encoding utf8 | ConvertFrom-Json
$известныеВиды = @($тр.виды.PSObject.Properties.Name)
foreach ($в in $к.виды) {
    # дефолтные поля — тем же проходом (ревью П4): сегодня их типы задаёт генератор и в карте
    # их нет (guard -not типы), но если будущая карта их принесёт — квалификаторы обязаны быть
    $всеПоля = @($в.реквизиты) + @($в.дефолтныеПоля)
    foreach ($р in $всеПоля) {
        if (-not $р.типы) { continue }
        $имяПоля = if ($р.имяРеквизита) { $р.имяРеквизита } else { "$($р.имя)$($р.тч) (дефолтное)" }
        foreach ($т in $р.типы) {
            if ($т.вид -notin $известныеВиды) { Fail "$($в.справочник.имя).${имяПоля}: вид типа '$($т.вид)' не покрыт ТипыРеквизитов.json"; continue }
            switch ($т.вид) {
                { $_ -in 'Число','ЧислоНеотрицательное' } {
                    if (-not $т.разрядность) { Fail "${имяПоля}: Число без разрядности" } }
                'Строка' { if (-not $т.длина) { Fail "${имяПоля}: Строка без длины" } }
                'Ссылка' { if (-not $т.ссылочныйТип) { Fail "${имяПоля}: Ссылка без ссылочныйТип" } }
            }
        }
        if ($р.индексировать -and ($р.типы | Where-Object { $_.вид -eq 'СтрокаНеограниченная' })) {
            Fail "${имяПоля}: индексация неограниченной строки запрещена"
        }
        if (@($р.типы).Count -gt 1 -and ($р.типы | Where-Object { $_.вид -eq 'СтрокаНеограниченная' })) {
            Fail "${имяПоля}: неограниченная строка в составном типе запрещена платформой"
        }
    }
    # такт 5 П4: приземлённое сведение (решение=реквизитОбъекта) физически в колонке — механизм
    # свойств ему не нужен. Хвост = EAV-остаток реквизитов + сведения, ОСТАВШИЕСЯ в РС (остаётсяВРС).
    # Двусторонний гейт: false при живом хвосте — потеря механизма; true без хвоста — мёртвый набор.
    $сведВРСВида = @($в.допСведения | Where-Object { $_.решение -eq 'остаётсяВРС' }).Count
    $ожидНужен = (@($в.остаютсяВEAV).Count -gt 0) -or ($сведВРСВида -gt 0)
    if ($ожидНужен -ne [bool]$в.eavХвост.нуженМеханизмСвойств) {
        Fail "$($в.справочник.имя): eavХвост.нуженМеханизмСвойств=$($в.eavХвост.нуженМеханизмСвойств), а по хвосту (EAV=$(@($в.остаютсяВEAV).Count), сведВРС=$сведВРСВида) ожидалось $ожидНужен (критика №23)"
    }
}

# Вердикт
Write-Host ''
if ($ошибки.Count -eq 0) {
    Write-Host "=== ВЕРДИКТ: ЗЕЛЕНО (видов=$видов, реквизитов=$тащим) ===" -ForegroundColor Green
    exit 0
} else {
    Write-Host "=== ВЕРДИКТ: КРАСНО, ошибок: $($ошибки.Count) ===" -ForegroundColor Red
    exit 2
}
